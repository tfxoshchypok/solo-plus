/**
 * Дрібний помічник доступу до рядка за ключем-шляхом, напр. t('app.title').
 * @param {string} key
 * @returns {string}
 */
export function t(key) {
  const val = key.split('.').reduce(
    /** @param {any} acc @param {string} part */
    (acc, part) => (acc && typeof acc === 'object' ? acc[part] : undefined),
    /** @type {any} */ (uk),
  )
  return typeof val === 'string' ? val : key
}
